## Function to Install and|OR load packages from CRAN
installNloadCRANpackages <- function(requiredPackages, lib) {
  for(p in requiredPackages) {
    if(p %in% rownames(installed.packages()) == FALSE) {
      cat("\nInstalling",p,"from CRAN in",lib,"...\n")
      .libPaths(lib)
      install.packages(p, repos = "https://cloud.r-project.org")
      suppressMessages(library(p, character.only = T))
    } else {
      cat("\nExcellent", p, "already installed. Loading", p, "...")
      suppressMessages(library(p, character.only = T))
    }}
  cat("\n\tDone setting up packages..\n\tWorking Directory is:",getwd())
}

