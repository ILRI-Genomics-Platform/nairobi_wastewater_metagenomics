# Function to read and filter kreport reports: select root; unclassified; domain; phylum
# The output is used in the generation of summary statistics about the taxonomic coverage of the runs.
read_KReports <- function(kreportFilesdf,idColName) {
  #outputs=c()
  kreportsList <- list()
  for(fileName in kreportFilesdf$ReportFilePath){
    output<-kreportFilesdf[match(fileName,kreportFilesdf$ReportFilePath),idColName]
    cat("Reading",output, "\n")
    report <- read.csv(file = fileName, header = F, sep = "\t", quote = "", 
                       col.names = c("percentage","cladeReads","taxonReads","taxRank","taxID","name"))
    report$name <- gsub('+5"',"+5", report$name, fixed=TRUE)
    # gsub("'","", report$name, fixed=TRUE)
    # cat("Processing step0 \n")
    if (all(c("name","taxRank") %in% colnames(report)) && !"taxLineage" %in% colnames(report)) {
      ## Kraken report
      report$depth <- nchar(gsub("\\S.*","",report$name))/2
      if (!all(report$depth == floor(report$depth))) {
        warning("Depth doesn't work out!")
        return(NULL)
      }
      # cat("Processing step1 \n")
      report$name <- gsub("^ *","",report$name)
      report$name <- paste(tolower(report$taxRank),report$name,sep="_")
      rownames(report) <- NULL
      ## make taxLineage path
      report$taxLineage <- report$name
      n <- nrow(report)
      depths <- report$depth
      taxLineages <- report$name
      taxLineages_p <- as.list(seq_along(report$name))
      
      depth_row_tmp <- c(1:25)
      
      # cat("Processing step2 \n")
      for (current_row in seq(from=1, to=nrow(report))) {
        dcr <- depths[current_row]
        depth_row_tmp[dcr+1] <- current_row
        if (dcr >= 1) {
          prev_pos <- depth_row_tmp[[dcr]]
          taxLineages_p[[current_row]] <- c(taxLineages_p[[prev_pos]], current_row)
        }
      }
      report$taxLineage <- sapply(taxLineages_p, function(x) paste0(taxLineages[x], collapse="|"))
      report <- rename_with(report, ~ paste(output, .x, sep = "."), any_of(c("percentage","cladeReads","taxonReads")))
      filePathOUT <- paste("./plotdata/abundance/allDomains/samples/",shortDate,"_",output,"_allTaxaReadAbundance.csv", sep = "")
      cat("Writing dataframe of",output," kreport...\n")
      write.table(report, file = filePathOUT, row.names = FALSE, col.names= TRUE, sep = "\t", quote = FALSE)
    }
    assign(output[1], report)
    kList <- get(output)
    kreportsList[[output]] <- kList }
  # return(kreportsList)
  cat("Merging all extracted Kreports into one dataframe")
  reportColNames <- c("taxRank","taxID","name","depth","taxLineage")
  kreportDf <- reduce(kreportsList, full_join, by = reportColNames) %>% 
    mutate_if(is.numeric,coalesce,0) %>%
    select(name,taxID,taxRank,starts_with("SPL0"),depth,taxLineage)
  return(kreportDf)
