read_jsonHostile <- function(hostileQCjson) {
  jsonQCList <- list()
  for(fileName in hostileQCjson){
    # For each path and extract name
    output<-sub("\\.hostile\\.logs\\.json$", "", basename(fileName))
    cat("Reading HOSTILE JSON from",output, "\n")
    # Initialize error flag
    error_occurred <- FALSE
    # Use tryCatch to handle errors
    tryCatch({
      # Step 1: Read the entire content of the file
      file_content <- readLines(fileName, encoding = "UTF-8")
      # Step 2: Extract the JSON part using regular expressions 
      # Find the index of the first line (after one that starts with "[") and end line before the one that starts with "]"
      start_index <- grep("^\\[", file_content)[1] + 1
      end_index <- grep("^\\]", file_content)[1] - 1
      json_part <- file_content[start_index:end_index]
      # Step 3: Read the JSON part into R    
      assign(output[1], jsonlite::fromJSON(txt = json_part, flatten = TRUE))
      qcList <- get(output)
      jsonQCList[[output]] <-qcList
    }, error = function(e) {
      cat("Error reading", output, ": ", e$message, "\n")
      error_occurred <- TRUE
    })
    # Skip to the next iteration if an error occurred
    if (error_occurred) {
      next
    }
  }
  return(jsonQCList)
}

