#Function to extract dataframe and merge a list generated by read_jsonResfinder() function above
filter_ResfinderReports <- function(resFinderReportsList) {
  
  # Function to standardize the length of lists
  standardize_length <- function(lst) {
    max_length <- max(sapply(lst, length))
    lapply(lst, function(x) {
      length(x) <- max_length
      return(x)  }) }
  
  # Function to convert a vector to a single string separated by a specified delimiter
  convert_to_string <- function(x, field, delimiter = ":") {
    if (is.vector(x[[field]]) || is.list(x[[field]])) {
      x[[field]] <- paste(x[[field]], collapse = delimiter)
    }
    return(x)
  }
  
  # Read ResFinder Reports; perform convert_to_string(); perform standardize_length()
  AMRList <- list()
  DrugsList <- list()
  listNames <- names(resFinderReportsList)
  for(fileName in listNames){
    # fileName="COVG158"
    output <- fileName
    cat("Extracting HOSTILE summary stats from",output, "\n")
    # Initialize error flag
    error_occurred <- FALSE
    # Use tryCatch to handle errors
    tryCatch({
      seq_regions_df <- lapply(resFinderReportsList[[fileName]]$seq_regions, function(region) {
        for (field in c("phenotypes","pmids")) {region <- convert_to_string(region, field)}
        return(region) }) %>% 
        lapply(., standardize_length) %>%
        bind_rows(., .id = "key") %>%
        mutate(sampleID = output) %>% select(sampleID, everything())
      phenotypes_df <- lapply(resFinderReportsList[[fileName]]$phenotypes, function(region) {
        for (field in c("amr_classes","seq_regions","seq_variations","ref_database")) {
          region <- convert_to_string(region, field, delimiter = ":") }
        return(region) }) %>% 
        lapply(., standardize_length) %>%
        bind_rows(., .id = "key") %>%
        mutate(sampleID = output) %>% select(sampleID, everything())
    }, error = function(e) {
      cat("Error reading", output, ": ", e$message, "\n")
      error_occurred <- TRUE
    })
    # Skip to the next iteration if an error occurred
    if (error_occurred) {
      next
    }
    AMRList[[output]] <- seq_regions_df 
    DrugsList[[output]] <- phenotypes_df}
  seqRegionsDF <- AMRList %>% bind_rows(.) %>% ungroup
  phenotypesDF <- DrugsList %>% bind_rows(.) %>% ungroup
  AMRDrugList <- list(seqRegionsDF = seqRegionsDF, phenotypesDF = phenotypesDF)
  return(AMRDrugList)
}

