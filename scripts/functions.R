# Function to merge rows
merge_rows <- function(df) {
  append_pipe <- function(x, readID) {
    if (length(x) == 1) {
      if (grepl("/1$", readID)) {
        return(paste0(x, "|"))
      } else {
        return(paste0("|", x))
      }
    } else {
      return(paste(x, collapse = "|"))
    }
  }
  
  df %>%
    summarise(
      sampleID = first(sampleID),
      databaseID = first(databaseID),
      drugClass = first(drugClass),
      amrClass = first(name),
      read = append_pipe(read, first(readID)),
      mappedTemplates = append_pipe(mappedTemplates, first(readID)),
      mappingScore = append_pipe(mappingScore, first(readID)),
      startPosition = append_pipe(startPosition, first(readID)),
      endPosition = append_pipe(endPosition, first(readID)),
      templateID = first(templateID),
      readID = if (n() == 2) {
        paste(unique(sub("/[12]$", "", readID)), collapse = "|") %>% paste0("/1|2")
      } else if (n() == 1) {
        paste0(sub("/[12]$", "", readID), "/1|2")
      } else {
        stop(paste("Error while reading", unique(sub("/[12]$", "", readID)), ": we found", n(), "number of readIds..."), call. = FALSE)
      }
    )
}

