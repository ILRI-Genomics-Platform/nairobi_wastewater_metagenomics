      write.table(kraken2Output0, file = kraken2Output0_file, row.names = F, col.names = F, sep = "\t", quote = F)
      
      # Kraken2 Krona Input
      kraken2Krona <- merged_data %>% select(V1,V3.y)
      kraken2Krona_file = paste(getwd(),"/rgi/stats/",output,".ARG-Kraken2.krona.tsv", sep="")
      write.table(kraken2Krona, file = kraken2Krona_file, row.names = F, col.names = F, sep = "\t", quote = F)
      
      # Populate the output dataframe with Sample names and output paths
      outputdf <- tibble::tibble(sampleID = output, mergedARGKraken2_file = merged_data_file, 
                                 kraken2SOutput_file = kraken2Output0_file, kraken2Krona_file = kraken2Krona_file)
      sink()
      return(outputdf)
    }, error = function(e) {
      # Handle the error
      cat("An error occurred:", e$message, "\n")
    }, finally = {
      sink()
    })
  }
  
  stopCluster(myCluster)
  outputdf <- bind_rows(outputdf0)
  return(outputdf)
}

